package ru.job4j.condition;

public class Liken {
    public static void main(String[] args) {
     
        int first = 5;
        int second = 8;
        boolean rsl;

        rsl = first > second;
        System.out.println(rsl);

        rsl = first < second;
        System.out.println(rsl);

        rsl = first == second;
        System.out.println(rsl);
    }
}
